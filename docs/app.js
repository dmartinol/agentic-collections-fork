function renderCollectionPage(pack) {
    const body = document.getElementById('collection-page-body');
    if (!body) return;
    body.textContent = '';

    const yamlLink = document.getElementById('collection-yaml-link');
    const blob = githubBlobBase();
    if (yamlLink) {
        if (blob) {
            yamlLink.href = `${blob}/${pack.name}/.catalog/collection.yaml`;
            yamlLink.removeAttribute('hidden');
        } else {
            yamlLink.href = '#';
            yamlLink.setAttribute('hidden', '');
        }
    }

    const readmeLink = document.getElementById('collection-readme-link');
    if (readmeLink) {
        if (blob && pack.has_readme) {
            readmeLink.href = `${blob}/${pack.name}/README.md`;
            readmeLink.removeAttribute('hidden');
        } else {
            readmeLink.href = '#';
            readmeLink.setAttribute('hidden', '');
        }
    }

    const c = pack.collection;

    const licenseNavLink = document.getElementById('collection-license-link');
    if (licenseNavLink) {
        const legal = c && c.legal_resources ? c.legal_resources : {};
        licenseNavLink.href = legal.license_agreement_url ? String(legal.license_agreement_url) : UPSTREAM_REPO_LICENSE_URL;
    }
    const titleText = (c && c.name) ? c.name : (pack.plugin.title || pack.plugin.name || pack.name);
    const h1 = document.createElement('h1');
    h1.className = 'collection-page-title';
    h1.textContent = titleText;
    body.appendChild(h1);

    if (c && c.description) {
        const sub = document.createElement('p');
        sub.className = 'collection-page-sub';
        const d = String(c.description);
        sub.textContent = d.length > 120 ? `${d.slice(0, 120).trim()}…` : d;
        body.appendChild(sub);
    }

    const metaLine = document.createElement('p');
    metaLine.className = 'collection-page-sub';
    const modLab = document.createElement('strong');
    modLab.textContent = 'Module:';
    metaLine.appendChild(modLab);
    metaLine.appendChild(document.createTextNode(` ${pack.name}`));
    if (pack.plugin && pack.plugin.version) {
        metaLine.appendChild(document.createTextNode(` · v${pack.plugin.version}`));
    }
    if (c && Array.isArray(c.personas) && c.personas.length) {
        metaLine.appendChild(document.createTextNode(` · ${c.personas.join(', ')}`));
    }
    body.appendChild(metaLine);

    if (!c) {
        const note = document.createElement('p');
        note.className = 'collection-missing';
        note.textContent =
            'Structured catalog metadata is not included in the published site data for this pack yet. See the repository README on GitHub.';
        body.appendChild(note);
        if (pack.has_readme && blob) {
            const a = document.createElement('a');
            a.href = `${blob}/${pack.name}/README.md`;
            a.target = '_blank';
            a.rel = 'noopener noreferrer';
            a.textContent = 'README on GitHub →';
            a.className = 'collection-meta-link';
            body.appendChild(a);
        }
        return;
    }

    if (c.categories && c.categories.length) {
        const catRow = document.createElement('p');
        catRow.className = 'collection-tags';
        const catLab = document.createElement('strong');
        catLab.textContent = 'Categories:';
        catRow.appendChild(catLab);
        catRow.appendChild(document.createTextNode(` ${c.categories.join(', ')}`));
        body.appendChild(catRow);
    }

    const tabStrip = document.createElement('div');
    tabStrip.className = 'collection-tabs';
    tabStrip.setAttribute('role', 'tablist');

    const tabDefs = [
        { id: 'overview', label: 'Overview' },
        { id: 'skills', label: 'Skills' },
        { id: 'resources', label: 'Resources' },
        { id: 'agents', label: 'Agents' }
    ];
    tabDefs.forEach((t, i) => {
        const b = document.createElement('button');
        b.type = 'button';
        b.className = `collection-tab${i === 0 ? ' active' : ''}`;
        b.dataset.tab = t.id;
        b.textContent = t.label;
        b.setAttribute('role', 'tab');
        b.setAttribute('aria-selected', i === 0 ? 'true' : 'false');
        b.setAttribute('aria-controls', `tab-${t.id}`);
        tabStrip.appendChild(b);
    });
    body.appendChild(tabStrip);

    const pOverview = document.createElement('div');
    pOverview.id = 'tab-overview';
    pOverview.className = 'collection-tab-panel active';
    pOverview.setAttribute('role', 'tabpanel');
    buildCollectionOverviewPanel(pOverview, pack, c, blob);
    body.appendChild(pOverview);

    const pSkills = document.createElement('div');
    pSkills.id = 'tab-skills';
    pSkills.className = 'collection-tab-panel';
    pSkills.setAttribute('role', 'tabpanel');
    buildCollectionSkillsPanel(pSkills, pack, c, blob);
    body.appendChild(pSkills);

    const pRes = document.createElement('div');
    pRes.id = 'tab-resources';
    pRes.className = 'collection-tab-panel';
    pRes.setAttribute('role', 'tabpanel');
    buildCollectionResourcesPanel(pRes, pack, c, blob);
    body.appendChild(pRes);

    const pAgents = document.createElement('div');
    pAgents.id = 'tab-agents';
    pAgents.className = 'collection-tab-panel';
    pAgents.setAttribute('role', 'tabpanel');
    buildCollectionAgentsPanel(pAgents, pack, c);
    body.appendChild(pAgents);

    wireCollectionTabs(body);

    if (c.repository) {
        const foot = document.createElement('p');
        foot.className = 'collection-footer-meta';
        foot.textContent = String(c.repository);
        body.appendChild(foot);
    }
}

